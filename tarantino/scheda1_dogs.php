                    parte di Mr. Pink. Robert Forster, Timothy Carey e Seymour Cassel fecero un'audizione per il ruolo di Joe Cabot, mentre Samuel L. Jackson fece un provino per la parte di Mr. Orange, ma non ottenne il ruolo. Tarantino tuttavia
