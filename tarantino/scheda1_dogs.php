                    <h2>Produzione</h2>
                    • Tarantino aveva intenzione di girare il film in pellicola da 16 millimetri con un budget di 30.000 dollari: in questa versione del film, Lawrence Bender - il produttore - avrebbe interpretato Eddie il Bello.<br>
                    • Bender stava frequentando un corso di recitazione, e la moglie del suo insegnante, venuta a sapere del progettato film, presentò una copia della sceneggiatura ad Harvey Keitel che ne fu subito entusiasta e si offrì a Tarantino come coproduttore.
                    • Con Keitel nel cast, il fondo di produzione arrivò a 1.200.000 dollari.<br>
                    • Il budget del film era così basso che, ad esempio, la giacca da jogging sfoggiata dal personaggio di Eddie il Bello, apparteneva personalmente all'attore.<br>
                    • Gran parte del denaro che è stato usato per il film è stato speso per i vestiti dei sei membri, in particolare per la giacca elegante di Eddie il Bello, per gli stivali neri di Mr. Blonde e per i jeans neri di Mr. Pink.<br>
                    • Ne Le iene i personaggi indossano il modello Wayfarer degli occhiali Ray Ban, mentre i vestiti furono invece creati da Agnès B., famosa stilista. Per la realizzazione europea, il distributore usò poster differenti per ogni personaggio principale.<br>
                    • I vestiti delle sei iene sono ispirati a quelli dei personaggi di A Better Tomorrow II di John Woo del 1987 e dei Blues Brothers dell'omonimo film del 1980.<br>
                    • L'uso dei nomi in codice ispirati ai colori per i sei rapinatori è un omaggio di Quentin Tarantino al film cult del 1974 Il colpo della metropolitana di Joseph Sargent, in cui i protagonisti si chiamavano signor Green, signor Gray, signor Blue e signor Brown.
                    • Il capannone utilizzato nelle riprese era un deposito funebre in costruzione. Mr. Blonde si siede su un carro funebre in una scena. L'automobile di Mr. Blonde è una Cadillac Coupe de Ville: l'auto realmente apparteneva all'attore Michael Madsen. Le scene in cui Quentin Tarantino è davanti alla macchina da presa sono state girate da Robert Rodriguez.<br>
                    • Il titolo Reservoir Dogs è stato ispirato dalla difficoltà che Tarantino ha nel pronunciare il titolo del film Au revoir, les enfants di Louis Malle, che chiamava "The Reservoir film", unito a Straw Dogs di Sam Peckinpah.<br>
                    • All'inizio del film Tarantino definisce Like a Virgin di Madonna «una metafora della fava grossa». Per tutta risposta Madonna regalò al regista una copia del suo album Erotica con la dedica: «È una canzone che parla d'amore, non di fave grosse. Madonna».<br>
                    • La parola "fuck" nel film viene pronunciata 252 volte.<br>
                    • Nell'edizione speciale del DVD uscita negli Usa nel 2002, sono mostrate cinque sequenze che sono state tolte dalla versione finale del film: tre di esse sono linee di dialogo eliminate dal film, tra cui l'unica comprendente un personaggio femminile, un'agente di polizia collega di Mr. Orange. Le altre due sono diverse angolature della tortura di Marvin Nash, mostrata con maggiore uso di effetti splatter: una è ripresa da dietro invece che da davanti come nella versione inserita nel montaggio finale, mentre l'altra mostra direttamente l'orecchio venire afferrato e amputato in primo piano dalla mano di Mr. Blonde, accompagnato da un'abbondante fuoriuscita di sangue. Nella versione ufficiale non viene mostrata direttamente l'amputazione, perché la cinepresa si sposta sul lato sinistro dello schermo, e viene infine mostrato Mr. Blonde parlare con l'agente in agonia tenendo l'orecchio mozzato in mano.<br>
                    • Durante la medesima scena, Michael Madsen fu inizialmente messo a disagio dalla frase che Kirk Baltz recita ("Ho un bambino piccolo"). Madsen, che era diventato padre da poco tempo, ebbe difficoltà a recitare quella scena.<br>
                    • Su alcune copie del DVD, non appena Baltz finisce la frase, si può sentire una voce in sottofondo che dice "Oh, no, no..": è la voce di Michael Madsen, avverso nel girare la scena.<br>
                    • All'inizio della scena in cui Eddie telefona mentre guida, si vede passare un palloncino arancione: Tarantino inserì questo particolare per fare capire agli spettatori chi era l'agente di polizia che aveva fatto la soffiata.<br><br>
